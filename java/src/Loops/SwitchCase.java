package Loops;

public class SwitchCase {

	public static void main(String[] args) {
		String color="RED";
		
		switch(color) {
		
		case "Black":
			System.out.println("This is Black");
			break;
		case "RED":
			System.out.println("This is red");
			break;
		case "Yellow":
			System.out.println("This is yellow");
			break;
		default:
			System.out.println("Nothing");
		
		}
	}

}
